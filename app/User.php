class User extends Authenticatable
{
    use Notifiable;
    use SoftDeletes;
    /**
     * The attributes that are mass assignable.
     *
     * @var array
     */
    protected $fillable = [
        'name', 'email', 'password',
    ];

    protected $appends = ['p_user','is_admin'];

    /**
     * The attributes that should be hidden for arrays.
     *
     * @var array
     */
    protected $hidden = [
        'password', 'remember_token',
    ];

    //获取安全柜
    public function getSafeCabinets()
    {
        return SafePlaceModel::where('user_id',$this->id)->get();
//        return $this->hasMany('App\SafePlaceModel','id','user_id');
    }

    public function getPUserAttribute()
    {
        return $this->hasOne('App\User','id','p_id')->get();
    }

    public function getIsAdminAttribute()
    {
        return (int)$this->is_admin;
    }
}
